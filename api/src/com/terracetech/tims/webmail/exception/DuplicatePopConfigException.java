package com.terracetech.tims.webmail.exception;

public class DuplicatePopConfigException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public DuplicatePopConfigException() {
	}

	public DuplicatePopConfigException(String arg0) {
		super(arg0);
	}

	public DuplicatePopConfigException(Throwable arg0) {
		super(arg0);
	}

	public DuplicatePopConfigException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
