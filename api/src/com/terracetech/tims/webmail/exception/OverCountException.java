package com.terracetech.tims.webmail.exception;

public class OverCountException extends Exception {

	public OverCountException() {
	}

	public OverCountException(String arg0) {
		super(arg0);
	}

	public OverCountException(Throwable arg0) {
		super(arg0);
	}

	public OverCountException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
