package com.terracetech.tims.webmail.exception;

public class VirusCheckException extends Exception {

	private static final long serialVersionUID = 1L;

	public VirusCheckException() {
	}

	public VirusCheckException(String arg0) {
		super(arg0);
	}

	public VirusCheckException(Throwable arg0) {
		super(arg0);
	}

	public VirusCheckException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
