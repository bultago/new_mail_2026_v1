package com.terracetech.tims.webmail.exception;

public class ProtocolException extends Exception {	
	private static final long serialVersionUID = 1714827943385138439L;
	
	public ProtocolException() {
		super();
	}

	public ProtocolException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ProtocolException(String arg0) {
		super(arg0);
	}

	public ProtocolException(Throwable arg0) {
		super(arg0);
	}

}
