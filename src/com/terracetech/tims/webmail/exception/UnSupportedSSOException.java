package com.terracetech.tims.webmail.exception;

public class UnSupportedSSOException extends Exception {

	private static final long serialVersionUID = 1L;

	public UnSupportedSSOException() {
		super();
	}

	public UnSupportedSSOException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UnSupportedSSOException(String arg0) {
		super(arg0);
	}

	public UnSupportedSSOException(Throwable arg0) {
		super(arg0);
	}

}
