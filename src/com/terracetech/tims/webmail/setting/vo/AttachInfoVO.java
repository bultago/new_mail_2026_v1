package com.terracetech.tims.webmail.setting.vo;

public class AttachInfoVO {

	private String expireday;
	
	private String download;
	
	private String maxMBSize;
	
	private String bigMaxMBSize;

	public String getExpireday() {
		return expireday;
	}

	public void setExpireday(String expireday) {
		this.expireday = expireday;
	}

	public String getDownload() {
		return download;
	}

	public void setDownload(String download) {
		this.download = download;
	}

	public String getMaxMBSize() {
		return maxMBSize;
	}

	public void setMaxMBSize(String maxMBSize) {
		this.maxMBSize = maxMBSize;
	}

	public String getBigMaxMBSize() {
		return bigMaxMBSize;
	}

	public void setBigMaxMBSize(String bigMaxMBSize) {
		this.bigMaxMBSize = bigMaxMBSize;
	}

	
}
